package dsaq;

public class Part2variables {
	public static void main(String[] args) {
		System.out.println("Hi");
		System.out.print("\"Aniru\rddha\"");//A carriage return moves the cursor to the beginning of the current line, effectively "returning" it to the left-most position.
		System.out.println("\n");
		int x = 10;
		float y = 12.25f;
		double z= 12432135.89999;
		boolean as = true;
		char aert = 'a';
		System.out.println("Integer "+x);
		System.out.println("Float "+y);
		System.out.println("double "+z);
		System.out.println("boolean "+as);
		System.out.println("char "+aert);
		//This is a comment
		/*Multiline comment
		 * Comment
		 */
	}

}