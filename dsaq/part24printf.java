package dsaq;

public class part24printf {
    public static void main(String[] args) {
        System.out.printf("Hi %-30c I have just started coding ",'!');//for width after the char
        System.out.println();
        System.out.printf("I am %1d years old", 22);//for width before the char
        System.out.println();
        System.out.printf("My percentile is %.2f", 88.98);

    }
}
